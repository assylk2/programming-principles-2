import math
import random

# Built-in math functions

print(min(5, 10, 25))
print(max(5, 10, 25))
print(abs(-7.25))
print(pow(4, 3))

# math module

print(math.sqrt(64))
print(math.ceil(1.4))
print(math.floor(1.4))
print(math.pi)

# random module

print(random.random())
print(random.randint(1, 10))

fruits = ["apple", "banana", "cherry"]
print(random.choice(fruits))